# Operators in python
'''
Assignment operators
Arithmetic operators
Comparison operators
Logical operators
Identity operators
Membership operators
Bitwise operators
'''
#Arithmetic operators
print("5 + 6 is ", 5+6)
print("5 - 6 is ", 5-6)
print("5 * 6 is ", 5*6)
print("5 / 6 is ", 5/6)
print("5 % 3 is ", 5%3) # t will give remainder
print("5 ** 6 is ", 5**6) # ** is an exponential operator
print("15 // 6 is ", 15//6)  # it will give integer value only
# Assignment operators
print('Assignment operators')
x = 5
print(x)
x+=7    # x = x+7
print(x)
x-=2    # x = x-2
print(x)
#Comparison operators
print('Comparison operators')
i = 5
print(i==5)
#Logical operators
print('Logical operators')
a = True
b= False
print(a and b)
print(a or b)
print(not b)
#Identity operators
print('Identity operators')
print(a is  b)
print(a is not b)
#Membership operators
print('Membership operators')
list = [ 31,  55 ,8 ,5, 1 ,2 ,3]
print(31 in list)
print(31 not in list)
# Bitwise Operator
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11
print('Bitwise Operator')
print(0 & 1)
print(2 | 1)

